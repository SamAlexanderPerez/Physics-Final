function distance(v,t){
  let d=v*t;
  return d;
}
function distanceV(d,t){
  let v=d/t;
  return v;
}
function distanceT(d,v){
  let t=d/v;
  return t;
}


function force(m,a){
  let f=m*a;
  return f;
}

function momentum(m,v){
  let impulse=m*v;
  return impulse;
}
function momentumV(p,m){
  let v=p/m;
  return v;
}
function momentumM(p,v){
  let m=p/v;
  return m;
}

function ke(m,v){
  let energy=m*v*v/2;
  return energy;
}
